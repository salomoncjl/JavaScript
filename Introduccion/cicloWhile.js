/*
    Ciclo While
    Es similar a for, pero con la diferencia de que solo tenemos un condicional.
    Mientras se cumpla el condicional se ejecutara el ciclo.
*/
let contador = 0;
while(contador <= 10 ){
    console.log(contador);
    contador++;
}

/*
    Ciclo Do While
    Es similar al ciclo while, con  la diferencia de que se ejecuta el bloque de codigo al menos una vez.
*/

let i = 0;
do{
    console.log(i);
    i++
} while(i <=10);
